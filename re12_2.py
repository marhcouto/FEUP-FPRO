def tic_tac_toe(board,player):
    matrix = [("A1",board[0]), ("A2",board[1]), ("A3",board[2]), ("B1",board[4]), ("B2",board[5]),
              ("B3",board[6]), ("C1",board[8]), ("C2",board[9]), ("C3",board[10])]
    player_matrix = [(x[0], x[1]) for x in matrix if x[1] == player or x[1] == " "]
    return player_matrix 
    
